package test.mathex


import jse.math.vector.Vectors

def vec = Vectors.linspace(0, 1, 10).opt().refreverse();

println(vec);
// 10-length Vector:
//    1.000   0.8889   0.7778   0.6667   0.5556   0.4444   0.3333   0.2222   0.1111   0.000

vec.fill([1.000, 0.8889, 0.7778, 0.6667, 0.5556, 0.4444, 0.3333, 0.2222, 0.1111, 0.000])
println(vec);
// 10-length Vector:
//    1.000   0.8889   0.7778   0.6667   0.5556   0.4444   0.3333   0.2222   0.1111   0.000

vec.fill([1.000, 0.8889, 0.7778, 0.6667, 0.5556, 0.4444, 0.3333, 0.2222, 0.1111, 0.000] as double[])
println(vec);
// 10-length Vector:
//    1.000   0.8889   0.7778   0.6667   0.5556   0.4444   0.3333   0.2222   0.1111   0.000

println(vec.data());
// [1.0, 0.8889, 0.7778, 0.6667, 0.5556, 0.4444, 0.3333, 0.2222, 0.1111, 0.0]

