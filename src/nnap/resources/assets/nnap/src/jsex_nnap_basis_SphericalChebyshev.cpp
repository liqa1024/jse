static inline void calL2_(jdouble *aCnlm, jdouble *rFp, jint aLMax, jboolean aNoRadial) {
    // l == 0
    jdouble tCnl0;
    jdouble tMul;
    jdouble *tFp = rFp;
    if (!aNoRadial) {
        tCnl0 = aCnlm[0];
        tMul = PI4;
        tFp[0] = tMul * (tCnl0*tCnl0);
        ++tFp;
    }
    // else
    for (jint l = 1; l <= aLMax; ++l) {
        jint tLen = l+l+1;
        jdouble rDot = JSE_NNAP::dot(aCnlm + (l*l), tLen);
        tMul = PI4/(jdouble)tLen;
        tFp[l-1] = tMul * rDot;
    }
}
